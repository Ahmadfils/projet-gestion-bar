    public void tb1()
    {
        String d="select * from facture order by numFacture desc";
        tab.setRowCount(0);
        try {
            st=con.connecion().createStatement();
            rs=st.executeQuery(d);
            tab.setRowCount(0);
            while(rs.next())
            {
              tab.addRow(new Object[]{
               rs.getString("numFacture"),
               rs.getString("numCommande"),
               rs.getString("date")
              });
            }
        
        } catch (Exception ex) {
          //JOptionPane.showMessageDialog(null,ex);  
        }
    }
