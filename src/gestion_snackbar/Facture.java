public void nonfacture()
{
    String q="select commande.numCommande,idCommande,commande.date,sum(quantite*prixVenteUnitaire) as 'resu' from commande,detail,produit where commande.numCommande=detail.numCommande and detail.numProduit=produit.numProduit and detail.numCommande not in (select numCommande from facture) group by commande.numCommande   ";
    tab2.setRowCount(0);
    try {
            st=con.connecion().createStatement();
            rs=st.executeQuery(q);
            while(rs.next())
            { 
                tab2.addRow(new Object[]{
                rs.getString("commande.numCommande"),
                rs.getString("idCommande"),
                rs.getString("resu"),
                rs.getString("commande.date")
                });
                
            }
        } catch (Exception ex) {
          
        }

}
